     A       ' GREATER    THAN THAT ALLOWED FOR BY DIMENSIONS')
 3110 FORMAT (/,'  ***ERROR***  VALUE OF MFG READ IN',I5,
     A       ' GREATER THA   N THAT ALLOWED FOR BY DIMENSIONS')
