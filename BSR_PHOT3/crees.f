          CHA2 = C1(IN1,IPT)
          DUM = DUM + BLP(KP)*CHA1*CHA2
  120   CONTINUE
  130   CONTINUE
        IF (DUM.EQ.0.0D0) GOTO 220
        CHN(IPT) = DUM
  140 CONTINUE
      RETURN
C
  150 CONTINUE
      DO 210 IPT = 1,NHY
        DUM = 0.0D0
        IN1 = 0
        NU1 = NUP1
        DO 190 KP = 1,NU
          INV = NU1
          NU1 = NU1 - 1
          K1 = NU1 - 1
          IF (K1.EQ.0) K1 = 1
          CHA1 = 0.0D0
          IND = ND1
          DO 170 KQ1 = 1,NU1
            INV = INV - 1
            IND = IND + 1
            S = 0.0D0
            DO 160 KD = 1,NCNL
              IJ = NJ(KD)
              S = S + V(IJ,INV,IPT)*AB2(KD,IND,IPT)
  160       CONTINUE
            CHA1 = CHA1 + S*ALP(KQ1,K1)
  170     CONTINUE
          IF (N1.NE.0) GOTO 180
          DUM = BLP(1)*CHA1
          GOTO 200
C
  180     CONTINUE
          IN1 = IN1 + 1
          CHA2 = C1(IN1,IPT)
          DUM = DUM + BLP(KP)*CHA1*CHA2
  190   CONTINUE
  200   CONTINUE
        IF (DUM.EQ.0.0D0) GOTO 220
        CHN(IPT) = DUM
  210 CONTINUE
      RETURN
C
  220 CONTINUE
      DO 230 IPT = 1,NHY
        CHN(IPT) = 0.0D0
  230 CONTINUE
      RETURN
C
  240 CONTINUE
      DO 250 KP = 1,NU
        BLP(KP) = ALP(KP,K)
  250 CONTINUE
      NUD = NU + ND
      IF (NAB.EQ.2) GOTO 280
      DO 270 IPT = 1,NHY
        DUM = 0.0D0
        IN2 = 0
        IND = NUD
        DO 260 KP = 1,NU
          IN2 = IN2 + 1
          IND = IND - 1
          DUM = DUM + AB1(I,IND,IPT)*C2(IN2,IPT)*BLP(KP)
  260   CONTINUE
        IF (DUM.EQ.0.0D0) GOTO 220
        CHN(IPT) = DUM
  270 CONTINUE
      RETURN
C
  280 CONTINUE
      DO 300 IPT = 1,NHY
        DUM = 0.0D0
        IND = NUD
        IN2 = 0
        DO 290 KP = 1,NU
          IN2 = IN2 + 1
