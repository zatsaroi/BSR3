C   ===========================
C
      MFGP2 = MFG + 2
      DO 260 I = 1,NCHF
        DO 250 J = 1,NCHOP
          WRITE (JFILE,3000) (RFGST(M,I,J),M=1,MFGP2)
  250   CONTINUE
  260 CONTINUE
      IF (IBUG.NE.2) RETURN
      WRITE (MT6,3010)
      DO 280 I = 1,NCHF
        DO 270 J = 1,NCHOP
          WRITE (MT6,3000) (RFGST(M,I,J),M=1,MFGP2)
  270   CONTINUE
  280 CONTINUE
C
 3000 FORMAT (5E16.7)
 3010 FORMAT (/,'  RADIAL FUNCTIONS IN ASYMPTOTIC REGION')
      END
