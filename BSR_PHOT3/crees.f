  260 CONTINUE
      IF (INTER.EQ.1) CALL INTERP(F1,1)
      DO 280 K = 1,KMETH
        M = JK(K)
        DO 270 I = 1,NCHF
          WRITE (JFILE,3230) (F2(K,I,J),J=1,M)
  270   CONTINUE
  280 CONTINUE
      IF (INTER.EQ.1) CALL INTERP(F2,2)
  290 CONTINUE
