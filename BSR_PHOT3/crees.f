        K = 0
        KU = 2
        DO 70 KK = 2,NDRV
          K = K + 1
          KU = KU + 1
          Y2 = Y2*Y1
          Y3 = Y2
          S = 0.0D0
          DO 50 KP = 1,KU
            IF (Y3.LT.YMIN) GOTO 60
            Y3 = TEMP*Y3
            S = S + ALP(KP,K)*Y3
   50     CONTINUE
   60     CONTINUE
          PP(KK,IPT) = S
   70   CONTINUE
   80 CONTINUE
C
      END
