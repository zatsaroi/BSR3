      IF (INTER.EQ.0) GOTO 370
      IF (IALT.LT.1) GOTO 360
      CALL INTERP(F1,1)
      CALL INTERP(F2,2)
  360 CONTINUE
      IF (ISOLN.EQ.0) GOTO 370
      IF (IALT.EQ.1) GOTO 370
      CALL INTERP(F0,3)
      CALL INTERP(F0D,4)
