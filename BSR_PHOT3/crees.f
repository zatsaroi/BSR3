C
C     I=POSITION IN VECTOR ARRAYS
C     NP=OVERALL DERIVATIVE NP-1, MUST BE G.E.1
C     NV=DERIVATE NV-1 OF POTENTIAL MATRIX
C     NV=0 IMPLIES SUBSTITUTE DELTA(I,K) FOR POTENTIAL MATRIX DERIVATIVE
