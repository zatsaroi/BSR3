        DO 210 I = 1,NCHF
          DO 180 J = 1,NCHOP
            F22(I,J) = 0.0D0
  180     CONTINUE
          DO 200 L = 1,NCHF
            DO 190 J = 1,NCHOP
              F22(I,J) = F22(I,J) + RRFG(I,L)*F12(L,J)
  190       CONTINUE
  200     CONTINUE
  210   CONTINUE
C
C   CALCULATE LINEAR COMBINATION ON NEW SIN AND COS SOLUTIONS
C
        DO 230 I = 1,NCHF
          DO 220 J = 1,NCHOP
            RFGST(MM,I,J) = F21(I,J) + F22(I,J)
  220     CONTINUE
  230   CONTINUE
  240 CONTINUE
