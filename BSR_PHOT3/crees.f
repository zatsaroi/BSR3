      IF (JFILE.EQ.0) GOTO 350
      IF (IALT.EQ.1) GOTO 350
      DO 320 K = 1,KMETH
        M = JK(K)
        DO 310 I = 1,NCHF
          WRITE (JFILE,3230) (F0(I,J,K),J=1,M)
  310   CONTINUE
  320 CONTINUE
