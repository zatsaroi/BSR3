      SGN = 1.0D0
      IF (E2(J).LT.0.0D0) SGN = -1.0D0
      Y1 = 0.0D0
      DO 10 IPT = 1,NHY
        Y1 = Y1 + HY
        Y2 = Y1*Y1
        Y3 = ZET(1,IPT)
        Y3 = Y2/ (Y3*Y3)
        PP(1,IPT) = (-SGN*Y2*Y2*Y3)*
     A              (1.0D0-SGN*1.25D0*Y3)
   10 CONTINUE
      IF (NDRV.EQ.1) RETURN
C
      S2 = SGN + SGN
      ALP(1,1) = S2 + SGN
      ALP(2,1) = -7.0D0
      ALP(3,1) = 5.0D0*SGN
      DO 30 KK = 3,NDRV
        K = KK - 1
        KM1 = K - 1
        KU = KK + 1
        ALP(1,K) = -KU*ALP(1,KM1)
        ALP(KU,K) = S2*KK*ALP(KK,KM1)
        DO 20 KP = 2,KK
          KS = KP - 1
          ALP(KP,K) = S2*KS*ALP(KS,KM1) - (KP+KK)*ALP(KP,KM1)
   20   CONTINUE
   30 CONTINUE
