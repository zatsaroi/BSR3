   90       CONTINUE
            CHA1 = CHA1 + S*ALP(KQ1,K1)
  100     CONTINUE
          IF (N1.NE.0) GOTO 110
          DUM = BLP(1)*CHA1
          GOTO 130
C
  110     CONTINUE
