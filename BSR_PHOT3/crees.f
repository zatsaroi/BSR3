      SUBROUTINE DCHAIN(CHN,I,NP,NV,NAB,ND,C1,N1,C2,N2)
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      PARAMETER (mchf=200,MLMX=5)
