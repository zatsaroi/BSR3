          WRITE (JFILE,3240) A(I,J)
  450   CONTINUE
  460 CONTINUE
      IF (IBUG.EQ.0) GOTO 500
      WRITE (MT6,3040)
      DO 470 I = M,NCHF
        WRITE (MT6,3260) (A(I,J),J=1,NCHOP)
  470 CONTINUE
      GOTO 500
C
  480 CONTINUE
      DO 490 I = 1,NCHF
        READ (IFILE,3240) D(I)
        WRITE (JFILE,3240) D(I)
  490 CONTINUE
      IF (IBUG.EQ.0) GOTO 500
      WRITE (MT6,3040)
      WRITE (MT6,3260) (D(I),I=1,NCHF)
  500 CONTINUE
