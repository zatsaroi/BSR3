        NJ(KVD) = KVD + I1
   60 CONTINUE
C
   70 CONTINUE
      DO 80 KP = 1,NU
        BLP(KP) = ALP(KP,K)
   80 CONTINUE
      ND1 = ND - 1
      NUP1 = NU + 1
      IF (NAB.GT.1) GOTO 150
