C   COMPUTE RADIAL FUNCTIONS AT R(M)
C   ================================
C
      DO 240 M = 1,MFGP1
        MM = M + 1
C
C   PUT SOLUTIONS AT R(M-1) IN F11 AND F12
C
        DO 130 I = 1,NCHF
          DO 120 J = 1,NCHOP
            F11(I,J) = F21(I,J)
            F12(I,J) = F22(I,J)
  120     CONTINUE
  130   CONTINUE
C
C   CALCULATE NEW SIN SOLUTIONS AND STORE IN F21(I,J)
C
        DO 170 I = 1,NCHF
          DO 140 J = 1,NCHOP
            F21(I,J) = 0.0D0
  140     CONTINUE
          DO 160 L = 1,NCHF
            DO 150 J = 1,NCHOP
              F21(I,J) = F21(I,J) + RFGST(MM,I,L)*F11(L,J)
  150       CONTINUE
  160     CONTINUE
  170   CONTINUE
